package br.com.unicred.api.dto;

public class CadastroDoacaoDTO {
	
	  private String nome;
	  private String email;
	  private String cpf;
	  private String cnpj;
	  private String numeroCartao;
	  private String nomeNoCartao;
	  private String dataExpedicao;
	  private String codigoVericacao;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getNumeroCartao() {
		return numeroCartao;
	}
	public void setNumeroCartao(String numeroCartao) {
		this.numeroCartao = numeroCartao;
	}
	public String getNomeNoCartao() {
		return nomeNoCartao;
	}
	public void setNomeNoCartao(String nomeNoCartao) {
		this.nomeNoCartao = nomeNoCartao;
	}
	public String getDataExpedicao() {
		return dataExpedicao;
	}
	public void setDataExpedicao(String dataExpedicao) {
		this.dataExpedicao = dataExpedicao;
	}
	public String getCodigoVericacao() {
		return codigoVericacao;
	}
	public void setCodigoVericacao(String codigoVericacao) {
		this.codigoVericacao = codigoVericacao;
	}

	  

}
